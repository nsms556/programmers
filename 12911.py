# 1119 연습문제 > 다음 큰 숫자
# https://programmers.co.kr/learn/courses/30/lessons/12911

def solution(n):
    answer = n+1
    c = bin(n).count('1')
    
    for i in range(n+1, 1000001):
        if bin(answer).count('1') == c :
            return answer
        else :
            answer += 1

n = 78
print(solution(n))

n = 15
print(solution(n))

